using System;

class Program
{
      static void Main()
      {
            Console.WriteLine("cos(45 degrees) = " + Math.Cos(45 * Math.PI / 180));
            Console.WriteLine("sin(45 degrees) = " + Math.Sin(45 * Math.PI / 180));
            Console.WriteLine("tan(45 degrees) = " + Math.Tan(45 * Math.PI / 180));
            Console.WriteLine("log(10 = " + Math.Log10(10));
            Console.WriteLine("log(e) = " + Math.Log(Math.E));
            Console.WriteLine("e^2 = " + Math.Exp(2));
            Console.WriteLine("2^3 = " + Math.Pow(2, 3));
            Console.WriteLine("sqrt(9) = " + Math.Sqrt(9));
            Console.WriteLine("abs(-10) = " + Math.Abs(-10));
            Console.WriteLine("round(2.5) = " + Math.Round(2.5));
            Console.WriteLine("round(2.4) = " + Math.Round(2.4));
            Console.WriteLine("floor(2.5) = " + Math.Floor(2.5));
            Console.WriteLine("ceiling(2.4) = " + Math.Ceiling(2.4));
      }
}