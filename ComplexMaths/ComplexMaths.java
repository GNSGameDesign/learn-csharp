public class ComplexMaths
{
      public static void main(String[] args)
      {
            System.out.println("cos(45 degrees) = " + Math.cos(Math.toRadians(45)));
            System.out.println("sin(45 degrees) = " + Math.sin(Math.toRadians(45)));
            System.out.println("tan(45 degrees) = " + Math.tan(Math.toRadians(45)));
            System.out.println("log(10) = " + Math.log(10));
            System.out.println("log10(10) = " + Math.log10(10));
            System.out.println("log2(10) = " + Math.log(10) / Math.log(2));
            System.out.println("sqrt(10) = " + Math.sqrt(10));
            System.out.println("cbrt(10) = " + Math.cbrt(10));
            System.out.println("pow(2, 3) = " + Math.pow(2, 3));
            System.out.println("exp(2) = " + Math.exp(2));
            System.out.println("abs(-10) = " + Math.abs(-10));
            System.out.println("ceil(10.1) = " + Math.ceil(10.1));
            System.out.println("floor(10.1) = " + Math.floor(10.1));
            System.out.println("round(10.1) = " + Math.round(10.1));
      }
}
