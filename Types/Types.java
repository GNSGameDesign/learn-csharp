public class Types
{
      public static void main(String[] args)
      {
            int a = 3; // integer or whole number, can be positive or negative
            float b = 3.14f; // float is a decimal number, can be positive or negative, is less expensive and less accurate than double
            double c= 3.14; // double is a decimal number, can be positive or negative, is a lot more expensive and accurate than float
            char d = 'a'; // char is a single character, must be surrounded by single quotes
            boolean e = true; // boolean is a true or false value
            String f = "Hello World!"; // String is a string of characters, must be surrounded by double quotes
            System.out.println(a);
            System.out.println(b);
            System.out.println(c);
            System.out.println(d);
            System.out.println(e);
            System.out.println(f);
      }
}