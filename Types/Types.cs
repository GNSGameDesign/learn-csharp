using System;

class Program
{
      static void Main()
      {
            int a = 3; // represents a integer or whole number, can be positive or negative
            double b = 3.14; // represents a decimal number, can be positive or negative, more expensive than float
            float c = 3.14f; // represents a decimal number, can be positive or negative, less expensive than double
            char d = 'a'; // represents a single character, must be surrounded by single quotes
            string e = "Hello, World!"; // represents a string of characters, must be surrounded by double quotes
            bool f = true; // represents a boolean value, can only be true or false
            Console.WriteLine(a);
            Console.WriteLine(b);
            Console.WriteLine(c);
            Console.WriteLine(d);
            Console.WriteLine(e);
            Console.WriteLine(f);
      }
}